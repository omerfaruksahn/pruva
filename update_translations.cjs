langs.forEach(lang => {
  const filePath = path.join('public/locales', lang + '.json');
  let data = {};
  if (fs.existsSync(filePath)) {
    data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  }
  
  if(!data.auth) data.auth = {};
  if(!data.market) data.market = {};
  if(!data.post_ad) data.post_ad = {};
  
  Object.assign(data.auth, translations[lang].auth);
  Object.assign(data.market, translations[lang].market);
  Object.assign(data.post_ad, translations[lang].post_ad);
  
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
  console.log('Updated ' + lang + '.json');
});
